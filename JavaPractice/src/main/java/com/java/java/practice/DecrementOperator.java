package com.java.java.practice;

public class DecrementOperator {

	public static void main(String[] args) {
		// ++ is increment Operator

		int a = 10;
		System.out.println(a);
		a++; // a=a+1;
		System.out.println(a);

		int result = a++;
		System.out.println(result);

		int res = ++a;
		System.out.println(res);
		System.out.println(a);
	}

}
