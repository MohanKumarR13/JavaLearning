package com.java.java.practice;

public class IncrementOperator {

	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		a--;
		System.out.println(a);

		int b = 100;
		int result = b--;
		System.out.println(result);
		System.out.println(b);

		int res = --b;
		System.out.println(res);
		System.out.println(b);
	}

}
